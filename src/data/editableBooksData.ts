// دالة للحصول على كتاب بواسطة المعرف
export const getBookById = (id: string | number): BookData | undefined => {
  return booksData.find(book => book.id.toString() === id.toString());
};

// دالة للحصول على الكتب المشابهة
export const getSimilarBooks = (bookId: string | number, category: string, limit: number = 4): BookData[] => {
  return booksData
    .filter(book => book.id.toString() !== bookId.toString() && book.category === category)
    .slice(0, limit);
};

// دالة للحصول على رابط تحميل الكتاب
export const getBookDownloadUrl = (bookId: string | number): string | undefined => {
  const book = getBookById(bookId);
  return book?.downloadUrl;
};

// استخراج التصنيفات من بيانات الكتب
export const getCategoriesFromBooks = (): Array<{id: number, name: string, icon: string, count: number}> => {
  const categoryCounts: Record<string, number> = {};
  
  booksData.forEach(book => {
    categoryCounts[book.category] = (categoryCounts[book.category] || 0) + 1;
  });
  
  const categoryIcons: Record<string, string> = {
    "روايات": "📚",
    "دين وعقيدة": "🕌",
    "تاريخ": "📜",
    "أدب": "✍️",
    "علوم": "🔬",
    "فلسفة": "🤔",
    "سير ذاتية": "👤",
    "تطوير الذات": "🌟"
  };
  
  return Object.entries(categoryCounts).map(([name, count], index) => ({
    id: index + 1,
    name,
    icon: categoryIcons[name] || "📖",
    count
  }));
};

// استخراج المؤلفين من بيانات الكتب
export const getAuthorsFromBooks = (): Array<{id: number, name: string, booksCount: number}> => {
  const authorCounts: Record<string, {id: number, name: string, count: number}> = {};
  
  booksData.forEach(book => {
    const authorKey = book.author.name;
    if (!authorCounts[authorKey]) {
      authorCounts[authorKey] = {
        id: book.author.id,
        name: book.author.name,
        count: 0
      };
    }
    authorCounts[authorKey].count++;
  });
  
  return Object.values(authorCounts).map(author => ({
    id: author.id,
    name: author.name,
    booksCount: author.count
  }));
};
